                    Solving mathematical problems is an art and so is designing good mathe-
matical problems. In fact, the latter is often harder. Like other art forms, it

is impossible to lay down unanimous standards of quality here. But generally,
a good problem should have a certain succinctness of form. Its statement

should be easily accessible to those who have completed, say, the higher secondary school (HSC). If at all it introduces some new definition or notation,

they should be illustrated to make the statement crystal clear. This applies
to its diction too. It should be precise but not clumsy. (It should not use ‘the
house of which I am an owner’ instead of ‘my house’!) The solution should
not follow as an obvious consequence of some standard result or trick.
A good problem should also be specific, and not open ended such as one
asking for an investigation or exploration or a characterisation. Occasionally,
a problem can ask if some statement is true or false. But that is more suitable
for research. Also a good problem should refrain from generalisation. Often
a generalisation is likely to detract its (possibly deceptive) simplicity and its
appeal. Let the proud solver have the pleasure of saying ‘More generally, our
solution shows that ....’. Again this is because a problem is different from a
result in a research article.<br><br>
A good problem often carries a built in hint. But again the degree of
such a hint crucially affects its quality. Too faint a hint makes the problem
dicey while too loud a hint reduces it to a drill. Sometimes asking a stronger
assertion gives an unwarranted hint. An example is the problem to show that
if 101 integers are picked from the integers 1 to 200, then among the selected
integers there are two distinct ones such that the smaller divides the larger.
One can, in fact, show that the ratio of these two integers is some power of
2. But that would spoil the problem.
Comparatively, there are less controversies about solutions. The sine qua
non of a solution, is, of course, that it should solve the problem! And there
are people who think that that is all there is to it. To some extent this
view is justified, if solving problems is considered as a sport, especially a
competitive sport such as the various mathematics Olympiads. Even when
viewed as an art, an artist is not supposed to say much about his/her own
work. His/her statement ought to end with the work itself. The solver rarely
<br><br>
1.reveals his/her thought process or the attempts that were tried, especially
the ones that failed. It is only the final success that gets recorded.

But while this may be all right in sports, a lot is missed. To really ap-
preciate the subtleties of a solution, the reader must be sufficiently mature.

When this is not the case, somebody has to train the reader, by pointing out
some tempting attempts that are destined to fail. Very frequently, the same
problem has several solutions, varying widely in terms of their approach,
ranging from very sophisticated to very elementary but tricky. Some of the
solutions are applicable to other similar problems. A comparative study of
their merits and demerits builds up the mathematical maturity of the reader
and often inspires him/her to try his/her hand on some other problems, and
sometimes, even to pose good problems to others.
That’s where a commentator or a critique comes in. His job is not to produce artists or sportsmen. Proverbially, they are born. But a good commentator can abridge the gap between these gifted persons and the laymen

and thereby enhance the appreciation of the former’s achievements by the
latter. This is especially true for mathematics where the building blocks of
the art form are pieces of deductive reasoning. It is also true in other art

forms such as literature whose appeal is not sensuous. Bradley is no Shake-
speare. But you need Bradley to appreciate Shakespeare’s greatness, at least

when your literary tastes are in the developmental stages. There are also
well known art critiques in other fields such as music and fine arts. Literary
critisism is now a well developed discipline of literature. Inevitably, there are
controversies. For example, Bradley is considered outdated by the modern
school. Such controversies are actually a sign of healthy growth.<br><br>

Even where a viewer’s enjoyment is direct, a good commentator can en-
hance the viewing experience. Every fan claps heartily when he sees Ten-
dulkar hit a boundary. He has a limited view from his seat. But a com-
mentator, aided by a battery of cameras and his expertise, points out how

difficult it was for the batsman to find a placing given the short delivery and
tight fielding and that truly brings out the exceptional qualities of the master

blaster. Similarly a film critique points out how some apparently unimpor-
tant shots add to the impact of a film or how some subdued piece of music

in the background raises the emotional appeal of a scene.
Sadly, in mathematics, there is very little writing in the form of critical
appreciation, especially of problems and their solutions. There is a distinct
class of people who love mathematics for its inexhaustible supply of problems
of all levels of difficulty. There are books and websites that cater to this
<br><br>
2. class : For example, there are sites that give solutions to the problems in
the IMO (International Mathematics Olympiad) or the higher level Putnam
competition. But they often presume a certain degree of maturity on the
part of the reader and refrain from elucidating the solutions.

Then again, there are guides for students preparing for competitive ex-
aminations like the entrance tests to reputed educational institutes. But

since these tests are often based on questions of the multiple choice formats,
the solutions given rarely bother abrout rigour. Their principal aim is to
somehow hit the answer in the shortest possibe time.
So the need for a book or an online source that assumes little more than a
sincere interest on the part of the reader and knowledge of higher secondary

school level mathematics and exposes him patiently to the charm of mathematics through problems and solutions remains unfulfilled. The present

wikipages are a humble attempt to make a beginning by writing serial com-
mentaries on selected problems. Generally, the problems covered in this series

will be of the IMO level. Some problems will be of Higher Secondary School

(HSC) level and of direct relevance to the aspirants of the various competi-
tive entrance tests at this level, such as those conducted by the IIT’s, the ISI

(Indian Statistical Institute) or the Chennai Mathematical Institute. But it
is hoped that many persons who are well past that stage will also enjoy this
series and revive their interest in mathematics, even though they may not
need this mathematics in their professions.
Some of the problems chosen in these wikipages are at a slightly higher

level. Still, they should be accessible to a genuinely interested reader. Normally, each wikipage will cater to a single problem or to a buch of several

related problems. At the end of each wikipage, there will be a problem whose
solution will appear in the next wikipage. The reason for this arrangement
is that the author believes that one can appreciate a solution only after one
has tried the problem honestly.
Readers’ comments are invired and may be sent to the Editor of the series.
They may also suggest problems for inclusion.
